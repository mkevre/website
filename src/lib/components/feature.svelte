        <Console
          source={terminal.source}
          dark={terminal.dark}
          narrow={terminal.narrow}
          shadow={terminal.shadow}
          skipToEnd={terminal.skipToEnd} />
      {/if}
      {#if image}
        <img
          src={image.src}
          alt={image.alt}
          class="{image.classNames} mx-auto"
